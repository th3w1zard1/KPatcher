def escape_xml(s: str) -> str:
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;")


def get_all_keys(english: dict) -> list:
    return sorted(english.keys())


def write_resx(path: Path, data: dict, all_keys: list, english: dict):
    lines = [HEADER.rstrip()]
    for key in all_keys:
        value = data.get(key, english.get(key, ""))
        v = escape_xml(value)
        lines.append(f'  <data name="{key}" xml:space="preserve"><value>{v}</value></data>')
    lines.append("</root>")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


