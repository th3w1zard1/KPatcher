def _read_english_from_resx(root: Path) -> dict:
    """Read (name, value) from PatcherResources.resx. Un-escapes XML entities in value."""
    resx_path = root / "PatcherResources.resx"
    text = resx_path.read_text(encoding="utf-8")
    # Match <data name="Key" ...><value>...</value></data>; value may span lines and contain \n
    pattern = re.compile(r'<data name="([^"]+)"[^>]*>\s*<value>([\s\S]*?)</value>\s*</data>')
    result = {}
    for m in pattern.finditer(text):
        key, raw_val = m.group(1), m.group(2)
        # Unescape for use in Python; we re-escape when writing
        val = (
            raw_val.replace("&lt;", "<")
            .replace("&gt;", ">")
            .replace("&quot;", '"')
            .replace("&amp;", "&")
        )
        result[key] = val
    return result


